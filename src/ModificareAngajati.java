    public ArrayList<Angajat> angajatList(){
        ArrayList<Angajat> angajatiList = new ArrayList<>();
        //Aducerea angajatilor in interfata prin conexiune si select
        try{
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url="jdbc:sqlserver://localhost:1527;databaseName=FermaAnimale;user=sa;password=SethHexFlame10";
            Connection con = DriverManager.getConnection(url);
            String query_1 = "SELECT * FROM Angajat";
            Statement stm = con.createStatement();
            ResultSet rss = stm.executeQuery(query_1);
            Angajat angajat;
            while(rss.next()){
                angajat = new Angajat(rss.getString("Nume"), rss.getString("Prenume"), rss.getString("CNP"), rss.getString("Salariu"), rss.getString("NrTelefon"));

                angajatiList.add(angajat);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        return angajatiList;
    }
