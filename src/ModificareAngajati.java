    public void show_angajat(){
        ArrayList<Angajat> list = angajatList();
        DefaultTableModel model = (DefaultTableModel)Table_angajati.getModel();
        Object[] row = new Object[5];
        for(int i = 0; i<list.size(); i++){

            row[0] = list.get(i).getNume();
            row[1] = list.get(i).getPrenume();
            row[2] = list.get(i).getCNP();
            row[3] = list.get(i).getSalariu();
            row[4] = list.get(i).getNrTelefon();
            model.addRow(row);
        }
    }
    
